public class Sediste {
    private int red;
    private int broj;


    public Sediste(int red, int broj){
        this.red = red;
        this.broj = broj;
    }
    public int getRed() {
        return red;
    }

    public int getBroj() {
        return broj;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public void setBroj(int broj) {
        this.broj = broj;
    }

}
